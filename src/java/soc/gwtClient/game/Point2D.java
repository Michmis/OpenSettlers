package soc.gwtClient.game;

public class Point2D
{
    private int x;
    private int y;
    /**
     * @return the x
     */
    public int getX()
    {
        return x;
    }

    /**
     * @return the y
     */
    public int getY()
    {
        return y;
    }
    public Point2D(int x, int y)
    {
        this.x=x;
        this.y=y;
    }
    
}
